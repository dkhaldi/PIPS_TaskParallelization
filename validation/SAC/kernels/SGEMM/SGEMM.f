      PROGRAM MAIN

      INCLUDE 'SGEMM_INC.f'

      INTEGER i, j
      INTEGER bef, af, diff
      INTEGER befRef, afRef, diffRef
      INTEGER CLOCK
      REAL alpha, beta
      REAL a(1:SIZE, 1:SIZE), b(1:SIZE, 1:SIZE), c(1:SIZE, 1:SIZE)
      REAL aRef(1:SIZE, 1:SIZE), bRef(1:SIZE, 1:SIZE), 
     & cRef(1:SIZE, 1:SIZE)

      LOGICAL success

      alpha = 3
      beta = 4
      DO 10 i = 1, SIZE
          DO 20 j = 1, SIZE
              a(i, j) = i
              b(i, j) = i
              c(i, j) = i
              aRef(i, j) = i
              bRef(i, j) = i
              cRef(i, j) = i
20        ENDDO 
10    ENDDO

      bef = CLOCK()
      CALL SGEMM(SIZE, SIZE, SIZE, SIZE, SIZE, alpha, a, SIZE, b, SIZE, 
     & beta, c, SIZE)
      af = CLOCK()

      befRef = CLOCK()
      CALL SGEMMREF(SIZE, SIZE, SIZE, SIZE, SIZE, alpha, aRef, SIZE, bRe
     & f, SIZE, beta, c, SIZE)
      afRef = CLOCK()

      success = .TRUE.

      DO 30 i = 1, SIZE
          DO 40 j = 1, SIZE
            IF(((c(i, j) - cRef(i, j)) / cRef(i, j)) .GT. 0.000001) THEN
                PRINT *, 'c(i, j)', c(i, j), 'cRef(i, j)', cRef(i, j)
                success = .FALSE.
            ENDIF
40        ENDDO 
30    ENDDO

      IF(success .EQV. .TRUE.) THEN
         PRINT *, 'SUCCESS'
      ELSE
         PRINT *, 'ERROR'
      ENDIF

      diff = af - bef
      diffRef = afRef - befRef
      PRINT *, 'time', diff
      PRINT *, 'reference time', diffRef

      RETURN
